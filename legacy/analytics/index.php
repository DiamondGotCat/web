// 集計関数
function getCount($pdo, $condition = "") {
    $sql = "SELECT COUNT(*) FROM access_logs";
    if ($condition) $sql .= " WHERE $condition";
    return $pdo->query($sql)->fetchColumn();
}

// 日別アクセス数（全期間分）
$dailyData = $pdo->query("
    SELECT 
        DATE(access_time) AS date_part,
        COUNT(*) as count
    FROM access_logs
    WHERE MONTH(access_time) = MONTH(CURDATE()) AND YEAR(access_time) = YEAR(CURDATE())
    GROUP BY date_part
    ORDER BY date_part
")->fetchAll(PDO::FETCH_ASSOC);

// 整形して slot にする
$dailyData = array_map(function ($row) {
    return [
        'date' => $row['date_part'],
        'count' => (int)$row['count'],
    ];
}, $dailyData);

// 分単位のアクセス集計
$todayRawData = $pdo->query("
    SELECT 
        DATE_FORMAT(access_time, '%H:') AS hour_part,
        LPAD(FLOOR(MINUTE(access_time)/10)*10, 2, '0') AS minute_part,
        COUNT(*) AS count
    FROM access_logs
    WHERE DATE(access_time) = CURDATE()
    GROUP BY hour_part, minute_part
    ORDER BY hour_part, minute_part
")->fetchAll(PDO::FETCH_ASSOC);

// フォーマット統一 (例: 14:00, 14:20, ...)
$todayData = array_map(function ($row) {
    return [
        'minute' => $row['hour_part'] . $row['minute_part'],
        'count' => (int)$row['count'],
    ];
}, $todayRawData);

// 国別ランキング（全期間）
$countryTotal = $pdo->query("
    SELECT country, COUNT(*) as count
    FROM access_logs
    WHERE MONTH(access_time) = MONTH(CURDATE()) AND YEAR(access_time) = YEAR(CURDATE())
    GROUP BY country
    ORDER BY count DESC
    LIMIT 99999
")->fetchAll(PDO::FETCH_ASSOC);

